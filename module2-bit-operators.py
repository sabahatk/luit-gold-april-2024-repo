#Bitwise Operators: &, |, ~, ^, <<, >>

first_bit = 1
second_bit = 1

print(first_bit & second_bit) #1

first_bit = 1
second_bit = 0

print(first_bit | second_bit) #1

first_bit = 0
second_bit = 0
print(first_bit ^ second_bit) #1

print(~0)
print(12 << 1)
print(12 << 2)
print(12 << 3)
print(12 >> 1)
print(12 >> 2)
